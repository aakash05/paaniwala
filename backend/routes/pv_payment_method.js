router.post(
  "/payment",
  [
    check("user_mobile")
      .not()
      .isEmpty(),
    check("payment_method")
      .not()
      .isEmpty(),
    check("details")
      .not()
      .isEmpty()
  ],
  async (req, res) => {
    try {
      const error = await validationResult(req);
      if (!error.isEmpty()) {
        return res.status(300).json({
          success: false,
          message: error
        });
      }
      const { user_mobile, payment_method, details, status } = req.body;
      const created_at = new Date();
      const updated_at = new Date();
      const deleted = "no";
      pool.query(
        "INSERT INTO pv_payment_method ( user_mobile,payment_method,details,status,created_at,updated_at,deleted) VALUES ($1,$2,$3,$4,$5,$6,$7) RETURNING * ",
        [
          user_mobile,
          payment_method,
          details,
          status,
          created_at,
          updated_at,
          deleted
        ],
        (error, result) => {
          if (error) {
            res.status(300).json({
              success: false,
              message: error
            });
          }
          if (result) {
            res.status(200).json({
              success: true,
              message: "payment data added"
            });
          }
        }
      );
    } catch (error) {
      console.log(error);
      res.status(300).json({
        success: false,
        error: error,
        message: "server side error"
      });
    }
  }
);
